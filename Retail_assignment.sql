-- Run a sales analysis query 
SELECT C_FIRST_NAME, C_LAST_NAME, I_ITEM_ID, SUM(SS_SALES_PRICE) AS TOTAL_SPENT
FROM RETAIL_MARKETING_DB.PUBLIC.CAMPAIGN_SALES 
GROUP BY 1, 2, 3 
ORDER BY TOTAL_SPENT DESC;

