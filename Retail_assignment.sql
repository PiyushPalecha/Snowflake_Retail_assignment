-- part 4,5 Database, Table, and Permissions
-- Create Database
CREATE OR REPLACE DATABASE RETAIL_MARKETING_DB;

-- Create Table from Sample Data
CREATE OR REPLACE TABLE RETAIL_MARKETING_DB.PUBLIC.CAMPAIGN_SALES AS
SELECT 
    s.SS_SOLD_DATE_SK,
    d.D_DATE AS SALE_DATE,
    s.SS_ITEM_SK,
    i.I_ITEM_ID,     
    c.C_CUSTOMER_SK,
    c.C_FIRST_NAME,
    c.C_LAST_NAME,
    s.SS_SALES_PRICE
FROM SNOWFLAKE_SAMPLE_DATA.TPCDS_SF10TCL.STORE_SALES s
JOIN SNOWFLAKE_SAMPLE_DATA.TPCDS_SF10TCL.DATE_DIM d ON s.SS_SOLD_DATE_SK = d.D_DATE_SK
JOIN SNOWFLAKE_SAMPLE_DATA.TPCDS_SF10TCL.CUSTOMER c ON s.SS_CUSTOMER_SK = c.C_CUSTOMER_SK
JOIN SNOWFLAKE_SAMPLE_DATA.TPCDS_SF10TCL.ITEM i ON s.SS_ITEM_SK = i.I_ITEM_SK
WHERE d.D_YEAR = 2002  
LIMIT 1000000;         

